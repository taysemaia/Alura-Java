package java_basico;

public class TestaCondicional {
    public static void main(String[] args) {

        int idade = 20;

        System.out.println("Testando condicionais.");

        if(idade >= 18){
            System.out.println("Você tem mais de 18 anos");
        }
        else{
            System.out.println("Você é menor de 18 anos.");
        }

    }
}
